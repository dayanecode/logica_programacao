
lista = ["DAYANE","10/04/92",31,True]
print(lista)

# append() insere um elemento no final da lista
lista.append("Filha Amada")
print(lista)

# insert() insere um elemento em uma posição específica da lista
lista.insert(1,"Maravilhosa")
print(lista)

# pop() remove o último item da lista
lista.pop()
print(lista)

# clear() limpa toda a lista
lista.clear()
print(lista)